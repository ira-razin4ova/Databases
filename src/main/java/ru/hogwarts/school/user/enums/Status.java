package ru.hogwarts.school.user.enums;

import ru.hogwarts.school.security.service.AppUserDetails;
import ru.hogwarts.school.user.User;

/**
 * Статус студента в университете (жизненный цикл).
 *
 * <p>Отражает текущее положение студента и влияет на доступ к системе.
 * Отличается от {@link Role}, которая определяет права доступа (что можно делать).
 *
 * <p>Возможные значения:
 * <ul>
 *   <li><b>ACTIVE</b> — активный студент. Начальный статус при зачислении.
 *       Имеет полный доступ к системе: участие в квестах, покупка мерча,
 *       просмотр баланса. Может выполнять роли STUDENT, STAROSTA, CURATOR, DIRECTOR</li>
 *   <li><b>ACADEMIC_LEAVE</b> — студент в академическом отпуске.
 *       Временно приостановлен. Может иметь ограниченный доступ
 *       (например, только просмотр профиля без возможности участвовать в квестах)</li>
 *   <li><b>DISMISSED</b> — отчисленный студент. Доступ к системе заблокирован,
 *       но данные сохраняются для истории и отчетности. Баланс может быть
 *       заморожен или обнулен</li>
 *   <li><b>GRADUATED</b> — выпускник. Завершил обучение. Может иметь
 *       ограниченный доступ (например, просмотр профиля, доступ к функциям
 *       альма-матер, участие в мероприятиях для выпускников)</li>
 * </ul>
 *
 * <p><b>Важно:</b> статус проверяется в {@link AppUserDetails} при создании
 * обертки для Spring Security. Пользователи со статусом DISMISSED не могут
 * войти в систему.
 *
 * @see User
 * @see AppUserDetails
 */
public enum Status {

    ACTIVE,
    ACADEMIC_LEAVE,
    DISMISSED,
    GRADUATED;
}
