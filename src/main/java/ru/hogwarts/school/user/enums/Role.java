package ru.hogwarts.school.user.enums;
/**
 * Роли пользователей в системе с иерархией прав доступа.
 *
 * <p>Иерархия (от меньших прав к большим):
 * <ul>
 *   <li>{@link #STUDENT} — базовый уровень: просмотр квестов, покупка мерча</li>
 *   <li>{@link #STAROSTA} — помощник куратора: видит только свой факультет,
 *       может начислять баллы студентам</li>
 *   <li>{@link #CURATOR} — управленец факультета: создание квестов, назначение старост,
 *       управление студентами своего факультета, создание мерча своего факультета </li>
 *   <li>{@link #DIRECTOR} — супер-администратор: полный доступ ко всей системе,
 *       управление кураторами, создание общих квестов и мерча</li>
 * </ul>
 *
 * <p>Права доступа реализованы через Spring Security и проверяются
 * аннотациями {@code @PreAuthorize} на методах сервисов.
 */

public enum Role {
    STUDENT,
    STAROSTA,
    CURATOR,
    DIRECTOR
}
